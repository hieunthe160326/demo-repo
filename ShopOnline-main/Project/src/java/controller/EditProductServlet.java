    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        CategoryDAO cd = new CategoryDAO();
        String id_raw = request.getParameter("id");
        String name = request.getParameter("name");
        String price_raw = request.getParameter("price");
        String describe = request.getParameter("describe");
        String materials = request.getParameter("materials");
        String releaseDate = request.getParameter("date");
        String image = request.getParameter("image");
        int id;
        double price;

        try {
            id = Integer.parseInt(id_raw);
            price = Double.parseDouble(price_raw);
            Products pNew = new Products(id, name, price, describe, materials, releaseDate, image);
            cd.update(pNew);
        } catch (NumberFormatException e) {
        }
        response.sendRedirect("adproducts");
        request.getRequestDispatcher("adproducts.jsp").forward(request, response);
    }

